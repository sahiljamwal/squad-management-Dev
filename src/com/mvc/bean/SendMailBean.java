package com.mvc.bean;

public class SendMailBean {

	private String email;
	private String fname;

	public void setEmail(String email) {
		this.email = email;
	}

	public String getEmail() {
		return email;
	}

	public void setFname(String fname) {

		this.fname = fname;
	}

	public String getFname() {
		return fname;
	}

}
